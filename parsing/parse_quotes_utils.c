#include "minishell.h"

void	ft_cpy_parsed_quotes_help(char *str, char *src, int *f)
{
	while (src[f[2]] && src[f[2]] != '\'' && src[f[2]] != '\"')
		str[(*f)++] = src[(f[2])++];
	if (src[f[2]] && src[f[2]] == '\'')
	{
		f[2]++;
		while (src[f[2]] && src[f[2]] != '\'')
		{
			if (src[f[2]] == 0 || (src[f[2]] <= 15 && src[f[2]] >= 7))
			{
				str[(*f)++] = '\\';
				str[(*f)++] = src[f[2]++];
			}
			else
				str[(*f)++] = src[f[2]++];
		}
		f[2]++;
	}
}
