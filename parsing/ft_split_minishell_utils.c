#include "minishell.h"

void	ft_fill_minishell_split(char const *s, char c, int *j, char *r)
{
	int	i;

	i = 0;
	while (s[(*j)] && s[(*j)] == c)
		(*j)++;
	while (s[(*j)] && s[(*j)] != c)
	{
		if (s[(*j)] == '\'')
		{
			r[i++] = s[(*j)++];
			while (s[(*j)] != '\'')
				r[i++] = s[(*j)++];
			r[i++] = s[(*j)++];
		}
		if (s[(*j)] == '\"')
		{
			r[i++] = s[(*j)++];
			while (s[(*j)] != '\"')
				r[i++] = s[(*j)++];
			r[i++] = s[(*j)++];
		}
		while (s[(*j)] && s[(*j)] != c && s[(*j)] != '\'' && s[(*j)] != '\"')
			r[i++] = s[(*j)++];
	}
	r[i] = '\0';
}
